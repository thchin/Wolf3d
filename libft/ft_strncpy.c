#include <string.h>

char	*ft_strncpy(char *dst, const char *src, size_t n)
{
	size_t	i;

	i = 0;
	while (src[i] != '\0' && i < n)
	{
		dst[i] = src[i];
		i += 1;
	}
	while (i < n)
	{
		dst[i] = '\0';
		i += 1;
	}
	return (dst);
}
