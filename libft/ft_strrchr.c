char	*ft_strrchr(const char *s, int c)
{
	char	*save;
	char	ch;

	save = 0;
	while (*s != '\0')
	{
		ch = *s;
		if (ch == c)
			save = (char *)s;
		s += 1;
	}
	if (c == '\0')
		save = (char *)s;
	return (save);
}
