int	ft_strequ(char const *s1, char const *s2)
{
	unsigned int	i;

	i = 0;
	if ((void *)0 == s1 || (void *)0 == s2)
		return (0);
	while (s1[i] != '\0' && s2[i] != '\0' && s1[i] == s2[i])
		i += 1;
	if (s1[i] != '\0' || s2[i] != '\0')
		return (0);
	return (1);
}
