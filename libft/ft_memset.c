#include <string.h>

void	*ft_memset(void *b, int c, size_t n)
{
	size_t	i;
	char	*ptr;

	i = 0;
	ptr = (char *)b;
	while (i < n)
	{
		*(char *)ptr = (char)c;
		ptr += 1;
		i += 1;
	}
	return (b);
}
