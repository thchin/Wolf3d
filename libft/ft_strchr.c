char	*ft_strchr(const char *s, int c)
{
	while (*s != (char)c && *s != '\0')
		s += 1;
	if (*s == '\0' && (char)c != '\0')
		return (0);
	if (*s == '\0' && (char)c == '\0')
		return ((char *)s);
	return ((char *)s);
}
