#include <string.h>

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t	i;

	i = 0;
	if ((void *)0 == s1 || (void *)0 == s2)
		return (0);
	while (s2[i] != '\0')
		i += 1;
	if (0 == i)
		return (1);
	i = 0;
	while (s1[i] != '\0' && s2[i] != '\0' && i < n && s1[i] == s2[i])
		i += 1;
	if (i == n || s1[i] == s2[i])
		return (1);
	return (0);
}
