#include "libft.h"

void	ft_putnbr(int n)
{
	int	exp;

	exp = 1;
	if (n < 0)
		ft_putchar('-');
	else
		n = -n;
	while (n / exp < -9)
		exp *= 10;
	while (exp > 0)
	{
		ft_putchar('0' - (n / exp));
		n %= exp;
		exp /= 10;
	}
}
