void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int	i;

	i = 0;
	if ((void *)0 != s && (void *)0 != f)
	{
		while (s[i] != '\0')
		{
			f(i, &s[i]);
			i += 1;
		}
	}
}
