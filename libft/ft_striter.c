void	ft_striter(char *s, void (*f)(char *))
{
	int	i;

	i = 0;
	if ((void *)0 != s && (void *)0 != f)
	{
		while (s[i] != '\0')
		{
			f(&s[i]);
			i += 1;
		}
	}
}
